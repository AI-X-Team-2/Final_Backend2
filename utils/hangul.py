#한글 한 글자(음절)를 입력받아 그 글자를 구성하는 초성, 중성, 종성으로 분리(분해)
def decompose_hangul(char):
    """한글 음절을 초성, 중성, 종성으로 분해합니다."""
    if '가' <= char <= '힣':         #가'부터 '힣' 사이의 한글 음절이 맞는지 확인
        #순서 계산 - 입력된 글자가 첫 글자인 '가'로부터 몇 번째 순서에 있는지 계산
        char_code = ord(char) - ord('가')
        
        #수학 공식 적용: 나눗셈과 나머지 연산을 이용해 위에서 구한 순서값을 초성, 중성, 종성의 순번으로 각각 변환합니다.
        chosung_index = char_code // (21 * 28)
        jungsung_index = (char_code % (21 * 28)) // 28
        jongsung_index = char_code % 28
        
        CHOSUNG = ['ㄱ', 'ㄲ', 'ㄴ', 'ㄷ', 'ㄸ', 'ㄹ', 'ㅁ', 'ㅂ', 'ㅃ', 'ㅅ', 'ㅆ', 'ㅇ', 'ㅈ', 'ㅉ', 'ㅊ', 'ㅋ', 'ㅌ', 'ㅍ', 'ㅎ']
        JUNGSUNG = ['ㅏ', 'ㅐ', 'ㅑ', 'ㅒ', 'ㅓ', 'ㅔ', 'ㅕ', 'ㅖ', 'ㅗ', 'ㅘ', 'ㅙ', 'ㅚ', 'ㅛ', 'ㅜ', 'ㅝ', 'ㅞ', 'ㅟ', 'ㅠ', 'ㅡ', 'ㅢ', 'ㅣ']
        JONGSUNG = ['', 'ㄱ', 'ㄲ', 'ㄳ', 'ㄴ', 'ㄵ', 'ㄶ', 'ㄷ', 'ㄹ', 'ㄺ', 'ㄻ', 'ㄼ', 'ㄽ', 'ㄾ', 'ㄿ', 'ㅀ', 'ㅁ', 'ㅂ', 'ㅄ', 'ㅅ', 'ㅆ', 'ㅇ', 'ㅈ', 'ㅊ', 'ㅋ', 'ㅌ', 'ㅍ', 'ㅎ']
        
        #결과 반환
        return CHOSUNG[chosung_index], JUNGSUNG[jungsung_index], JONGSUNG[jongsung_index]
    return char, '', ''     #(아니라면 입력된 글자를 그대로 반환)